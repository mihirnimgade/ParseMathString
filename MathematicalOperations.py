# Mathematical operations

def add(m, n):
    m = int(m)
    n = int(n)
    return m + n


def multiply(m, n):
    m = int(m)
    n = int(n)
    return m * n


def subtract(m, n):
    m = int(m)
    n = int(n)
    return m - n


def divide(m, n):
    m = int(m)
    n = int(n)
    return int(m / n)


def exponent(m, n):
    m = int(m)
    n = int(n)
    return int(m**n)
